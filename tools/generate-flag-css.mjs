const FLAG_ICONS_PATH = "public/assets/flags/4x3/";
const FLAG_CSS_PATH = "src/styles/flags.css";
const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const projectRoot = join(__dirname, "..");

const encodeSvgForCss = (svg) =>
  svg
    .replace(/\r?\n|\r/g, "")
    .replace(/\s{2,}/g, " ")
    .replace(/"/g, "'")
    .replace(/%/g, "%25")
    .replace(/#/g, "%23")
    .replace(/\{/g, "%7B")
    .replace(/\}/g, "%7D")
    .replace(/</g, "%3C")
    .replace(/>/g, "%3E");

export const generateFlagCss = () => {
  const flagIconsDir = join(projectRoot, "node_modules", "flag-icons", "flags", "4x3");
  const fallbackFlagPath = join(projectRoot, FLAG_ICONS_PATH, "xx.svg");
  const outputPath = join(projectRoot, FLAG_CSS_PATH);
  const missingFlags = [];

  let css = `/* Auto-generated by tools/generate-flag-css.mjs. Do not edit manually. */

.country-flag {
  background-position: center;
  background-repeat: no-repeat;
  background-size: cover;
  border-radius: 0.125rem;
  display: inline-block;
  flex: 0 0 auto;
  height: 0.875rem;
  width: 1.15rem;
}

`;

  for (const country of COUNTRIES) {
    const flagIconPath = join(flagIconsDir, `${country}.svg`);
    const localFlagPath = join(projectRoot, FLAG_ICONS_PATH, `${country}.svg`);
    const svgPath = existsSync(localFlagPath)
      ? localFlagPath
      : existsSync(flagIconPath)
        ? flagIconPath
        : fallbackFlagPath;

    if (!existsSync(svgPath)) {
      missingFlags.push(country);
      continue;
    }

    const svg = readFileSync(svgPath, "utf8");
    const encodedSvg = encodeSvgForCss(svg);

    css += `.country-flag--${country} {
  background-image: url("data:image/svg+xml,${encodedSvg}");
}

`;
  }

  mkdirSync(dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, css, "utf8");

  if (missingFlags.length > 0) {
    console.warn(`Skipped ${missingFlags.length} missing flag(s): ${missingFlags.join(", ")}`);
  }

  console.log(`Generated ${FLAG_CSS_PATH} with ${COUNTRIES.length - missingFlags.length} flag classes.`);
};

generateFlagCss();